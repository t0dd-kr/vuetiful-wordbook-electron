const chromedriver = require('chromedriver')
const { spawn } = require('child_process');
import { app, ipcMain, BrowserWindow } from 'electron'

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\')
}

let mainWindow
const winURL = process.env.NODE_ENV === 'development'
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow () {
  /**
   * Initial window options
   */
  mainWindow = new BrowserWindow({
    height: 900,
    useContentSize: true,
    width: 700
  })

  mainWindow.loadURL(winURL)

  mainWindow.on('closed', () => {
    mainWindow = null
  })
}
// import parser from './modules/parse'
// console.log(parser)
import parser from './modules/parse'

ipcMain.on('req-parse', (event, words) => {
  parser(words, (translations) => {
    event.sender.send('res-parse', {translations})
  })
})

app.on('ready', createWindow)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

chromedriver.start(['--port=9515', '--headless','--disable-gpu','--disable-extensions','--disable-dev-shm-usage', '--no-sandbox', 'log-level=3'])
